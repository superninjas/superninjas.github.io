def make_permalink(ml_id, category):
    """Gera permalink com link de afiliado"""
    return f"https://www.mercadolivre.com.br/p/{ml_id}?{AFFILIATE}"

def generate_products():
    """Gera os 500 produtos e salva nos arquivos JSON de cada categoria"""
    print("🥷 Radar Ninja — Gerando 500 produtos com link de afiliado...")
    total = 0

    for category, products_list in PRODUCT_NAMES.items():
        ml_ids = ML_IDS[category]
        images = IMAGES[category]
        output = []

        for i, (title, price, original_price) in enumerate(products_list):
            ml_id = ml_ids[i % len(ml_ids)]
            image = images[i % len(images)]
            discount = int(((original_price - price) / original_price) * 100)
            permalink = make_permalink(ml_id, category)

            product = {
                "id": ml_id,
                "title": title,
                "name": title,
                "price": price,
                "original_price": original_price,
                "image": image,
                "thumbnail": image,
                "permalink": permalink,
                "custom_category_slug": category,
                "custom_discount_pct": discount,
                "condition": "new"
            }
            output.append(product)

        file_path = ROOT / f"data/products_{category}.json"
        with open(file_path, "w", encoding="utf-8") as f:
            json.dump(output, f, indent=2, ensure_ascii=False)

        print(f"   ✅ {category}: {len(output)} produtos salvos em {file_path.name}")
        total += len(output)

    print(f"\n🎯 Total: {total} produtos gerados com sucesso!")
    print(f"   Link de afiliado: ?{AFFILIATE}")

if __name__ == "__main__":
    generate_products()
