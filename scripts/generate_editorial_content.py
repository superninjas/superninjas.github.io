if __name__ == '__main__':
    from pathlib import Path
    ROOT = Path(__file__).resolve().parents[1]
    
    # Gerar páginas institucionais
    for page_name, content in editorial_content.items():
        if page_name.startswith('noticias_'):
            # Notícias
            dir_name = page_name.replace('noticias_', '')
            page_dir = ROOT / 'noticias' / dir_name
        else:
            # Institucionais
            page_dir = ROOT / page_name
        
        page_dir.mkdir(parents=True, exist_ok=True)
        html_file = page_dir / 'index.html'
        
        html_content = generate_html_page(
            content['title'],
            content['intro'],
            f"<p>{content['body'].strip()}</p>"
        )
        
        html_file.write_text(html_content, encoding='utf-8')
        print(f"✅ {html_file.relative_to(ROOT)}")
    
    print("\n✅ Conteúdo editorial gerado com sucesso!")
